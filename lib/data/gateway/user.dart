abstract class User {
  String _id;
  String get id => this._id;
  int _views;
  int get views => this._views;
  int _loaded;
  int get loaded => this._loaded;
  String _name;
  String get name => this._name;
  String _email;
  String get email => this._email;
  String _dayOfBirth;
  String get dayOfBirth => this._dayOfBirth;
}
